package com.water.bean;

public class RegionCircleFormBean {
	
	private String regionCircleId;
	private String regionName;
	private String circleName;
	private int circleId;
	private int regionId;
	
	public String getRegionCircleId() {
		return regionCircleId;
	}
	public void setRegionCircleId(String regionCircleId) {
		this.regionCircleId = regionCircleId;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getCircleName() {
		return circleName;
	}
	public void setCircleName(String circleName) {
		this.circleName = circleName;
	}
	public int getCircleId() {
		return circleId;
	}
	public void setCircleId(int circleId) {
		this.circleId = circleId;
	}
	public int getRegionId() {
		return regionId;
	}
	public void setRegionId(int regionId) {
		this.regionId = regionId;
	}
	
	
}
