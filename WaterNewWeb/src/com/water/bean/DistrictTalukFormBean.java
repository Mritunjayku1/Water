package com.water.bean;

public class DistrictTalukFormBean {
	
	private String districtTalukId;
	private String districtName;
	private String talukName;
	private int talukId;
	private int districtId;
	
	public String getDistrictTalukId() {
		return districtTalukId;
	}
	public void setDistrictTalukId(String districtTalukId) {
		this.districtTalukId = districtTalukId;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public String getTalukName() {
		return talukName;
	}
	public void setTalukName(String talukName) {
		this.talukName = talukName;
	}
	public int getTalukId() {
		return talukId;
	}
	public void setTalukId(int talukId) {
		this.talukId = talukId;
	}
	public int getDistrictId() {
		return districtId;
	}
	public void setDistrictId(int districtId) {
		this.districtId = districtId;
	}
	
	
}
