package com.water.bean;

public class CircleDivisionFormBean {

	private String circleDivisionId;
	private String circleName;
	private String divisionName;
	private int divisionId;
	private int circleId;
	
	public String getCircleDivisionId() {
		return circleDivisionId;
	}
	public void setCircleDivisionId(String circleDivisionId) {
		this.circleDivisionId = circleDivisionId;
	}
	public String getCircleName() {
		return circleName;
	}
	public void setCircleName(String circleName) {
		this.circleName = circleName;
	}
	public String getDivisionName() {
		return divisionName;
	}
	public void setDivisionName(String divisionName) {
		this.divisionName = divisionName;
	}
	public int getDivisionId() {
		return divisionId;
	}
	public void setDivisionId(int divisionId) {
		this.divisionId = divisionId;
	}
	public int getCircleId() {
		return circleId;
	}
	public void setCircleId(int circleId) {
		this.circleId = circleId;
	}
		
	
}
