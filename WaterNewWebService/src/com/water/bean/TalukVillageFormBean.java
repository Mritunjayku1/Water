package com.water.bean;

public class TalukVillageFormBean {

	private String talukVillageId;
	private String talukName;
	private String villageName;
	private int villageId;
	private int talukId;
	
	public String getTalukVillageId() {
		return talukVillageId;
	}
	public void setTalukVillageId(String talukVillageId) {
		this.talukVillageId = talukVillageId;
	}
	public String getTalukName() {
		return talukName;
	}
	public void setTalukName(String talukName) {
		this.talukName = talukName;
	}
	public String getVillageName() {
		return villageName;
	}
	public void setVillageName(String villageName) {
		this.villageName = villageName;
	}
	public int getVillageId() {
		return villageId;
	}
	public void setVillageId(int villageId) {
		this.villageId = villageId;
	}
	public int getTalukId() {
		return talukId;
	}
	public void setTalukId(int talukId) {
		this.talukId = talukId;
	}
		
	
}
